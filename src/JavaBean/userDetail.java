package JavaBean;

public class userDetail {
    private int manNumber;
    private int womanNumber;
    private int number;
    private int counts;//总量

    public int getManNumber() {
        return manNumber;
    }

    public void setManNumber(int manNumber) {
        this.manNumber = manNumber;
    }

    public int getWomanNumber() {
        return womanNumber;
    }

    public void setWomanNumber(int womanNumber) {
        this.womanNumber = womanNumber;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getCounts() {
        return counts;
    }

    public void setCounts(int counts) {
        this.counts = counts;
    }
}
